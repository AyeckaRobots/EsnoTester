def fancy_print(lis):
    print('{')
    for pls in lis:
        print(f"\t'{pls['plscode']}': ", "{")
        for key, value in pls.items():
            if type(value) == str:
                print(f"\t\t'{key}': '{value}',")
            else:
                print(f"\t\t'{key}': {value},")
        print("\t},")
    print('}')

